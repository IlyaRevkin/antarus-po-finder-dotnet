namespace AntarusPoFinder.App.Views;

/// <summary>Потолок отрисовки выдачи поиска и подпись к нему.
///
/// Карточки выдачи лежат в обычном StackPanel — без виртуализации, то есть каждая нарисованная
/// карточка это живые элементы разметки. На широком запросе по второму диску («1», «01») совпадений
/// набирается десятками тысяч: живой прогон дал 23 446 карточек, и окно вставало намертво на минуты
/// — ровно та беда, от которой обход диска и уводили в фон. Поэтому карточек рисуется не больше
/// <see cref="MaxCards"/>, а сколько совпало на самом деле — видно в строке состояния.
///
/// Потолок общий для обоих путей выдачи по схемам: и для потоковой (по ходу обхода диска), и для
/// мгновенной из прогретого кэша — раньше кэш-путь рисовал вообще всё, и именно он и вешал окно.</summary>
internal static class SearchResultCap
{
    public const int MaxCards = 300;

    /// <summary>«23446 (показаны первые 300)» — или просто «17», когда показано всё.</summary>
    public static string Describe(int matched, int shown) =>
        matched > shown ? $"{matched} (показаны первые {shown})" : matched.ToString();
}
